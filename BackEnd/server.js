function escapeRegExp(text) {
  return text.replace(/[-[\]{}()*+?.,\\^$|#\\s]/g, '\\$&')
}

var searchStates = function (args) {
  if (args.query) {
    var query = args.query
    const regexp = new RegExp(escapeRegExp(query), 'i')
    return usStateData.filter((state) => state.name.match(regexp))
  } else {
    return usStateData
  }
}
var root = {
  states: searchStates,
}

// Create an express server and a GraphQL endpoint
var app = express()
app.use(cors())
app.use(
  '/graphql',
  graphqlHTTP({
    schema: schema,
    rootValue: root,
    graphiql: true,
  })
)
app.listen(4000)
